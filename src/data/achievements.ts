/**
 * Get a translated achievement based on current language
 */
export function getTranslatedAchievement(ach: Achievement): Achievement {
  const lang = i18n.language;
  if (lang === "pt-BR" || lang.startsWith("pt")) return ach;
  
  const langKey = lang.startsWith("es") ? "es" : "en";
  const t = achievementTranslations[langKey]?.[ach.id];
  if (!t) return ach;
  
  return { ...ach, title: t.title, description: t.description };
}

export function getAchievementById(id: string): Achievement | undefined {
  return achievements.find((a) => a.id === id);
}
